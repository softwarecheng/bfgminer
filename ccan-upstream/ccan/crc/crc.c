uint32_t crc32_ieee(uint32_t crc, const void *buf, size_t size)
{
	const uint8_t *p;

	p = buf;
	crc ^= ~0U;

	while (size--)
		crc = crc32_ieee_tab[(crc ^ *p++) & 0xFF] ^ (crc >> 8);

	return crc ^ ~0U;
}

const uint32_t *crc32_ieee_table(void)
{
	return crc32_ieee_tab;
}

